import { jsPDF } from "jspdf";

const INK = "#0a0b0d";
const PAPER = "#fcfbf7";
const LIME = "#d4f570";
const MUTED = "#6b7280";
const LINE = "#e5e7eb";
const EMERALD = "#0f5f3f";

const PAGE_W = 210;
const PAGE_H = 297;
const MARGIN = 18;
const CONTENT_W = PAGE_W - MARGIN * 2;

const fmtMoney = (n, ccy = "USD") =>
  new Intl.NumberFormat("en-US", { style: "currency", currency: ccy, minimumFractionDigits: 2 }).format(n || 0);

const fmtNgn = (n) =>
  "NGN " + new Intl.NumberFormat("en-US", { minimumFractionDigits: 2, maximumFractionDigits: 2 }).format(n || 0);

const fmtDate = (iso) => {
  if (!iso) return "—";
  const d = new Date(iso);
  return d.toLocaleDateString("en-GB", { day: "2-digit", month: "short", year: "numeric" });
};

const fmtDateTime = (iso) => {
  if (!iso) return "—";
  const d = new Date(iso);
  return d.toLocaleString("en-GB", { day: "2-digit", month: "short", year: "numeric", hour: "2-digit", minute: "2-digit" });
};

const ref = (id) => "QU-" + String(id || "").slice(0, 4).toUpperCase();

function drawHeader(doc, title) {
  doc.setFillColor(INK);
  doc.rect(0, 0, PAGE_W, 24, "F");

  doc.setFillColor(LIME);
  doc.roundedRect(MARGIN, 8, 8, 8, 1.5, 1.5, "F");
  doc.setFont("helvetica", "bold");
  doc.setFontSize(10);
  doc.setTextColor(INK);
  doc.text("X", MARGIN + 4, 13.8, { align: "center" });

  doc.setFont("helvetica", "bold");
  doc.setFontSize(13);
  doc.setTextColor(PAPER);
  doc.text("XaePay", MARGIN + 12, 14);

  doc.setFont("helvetica", "normal");
  doc.setFontSize(8);
  doc.setTextColor(LIME);
  doc.text(title.toUpperCase(), PAGE_W - MARGIN, 14, { align: "right" });
}

function drawFooter(doc, page, total) {
  const y = PAGE_H - 12;
  doc.setDrawColor(LINE);
  doc.setLineWidth(0.2);
  doc.line(MARGIN, y - 4, PAGE_W - MARGIN, y - 4);

  doc.setFont("helvetica", "normal");
  doc.setFontSize(7);
  doc.setTextColor(MUTED);
  doc.text("Generated by XaePay  |  Funds custody by licensed payment partner.  |  XaePay is a software/compliance layer that does not custody funds.", MARGIN, y);
  doc.text(`Page ${page} of ${total}`, PAGE_W - MARGIN, y, { align: "right" });
}

function drawLabelValue(doc, label, value, x, y, valueOpts = {}) {
  doc.setFont("helvetica", "normal");
  doc.setFontSize(7);
  doc.setTextColor(MUTED);
  doc.text(label.toUpperCase(), x, y);

  doc.setFont(valueOpts.mono ? "courier" : "helvetica", valueOpts.bold ? "bold" : "normal");
  doc.setFontSize(valueOpts.size || 10);
  doc.setTextColor(valueOpts.color || INK);
  const v = String(value ?? "—");
  doc.text(v, x, y + 5);
  return y + (valueOpts.lineHeight || 11);
}

